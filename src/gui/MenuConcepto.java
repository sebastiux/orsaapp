    private void guardarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_guardarMousePressed
        // TODO add your handling code here:
        String key = gclave.getText();
        String desc = gdescripcion.getText();
        String costo = gcosto.getText();
        String idconcepto = id.getText();
        if(nuevo.isSelected()){
           tabla.insertarconcepto(key, desc, costo);
        }
        if(modificar.isSelected()){
            tabla.updateconcepto(idconcepto,key,desc,costo);
        }
        tabla.tablamenuc(jTable1);
    }//GEN-LAST:event_guardarMousePressed

