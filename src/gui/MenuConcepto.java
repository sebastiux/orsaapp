    private void modificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_modificarMouseClicked
        // TODO add your handling code here:
        nuevo.setSelected(false);
        if(modificar.isSelected()){
            
            gclave.setEnabled(true);
            gcosto.setEnabled(true);
            gdescripcion.setEnabled(true);
            
        }
        
        if(!modificar.isSelected() && !nuevo.isSelected()){
            gclave.setEnabled(false);
            gcosto.setEnabled(false);
            gdescripcion.setEnabled(false);
            
        }
    }//GEN-LAST:event_modificarMouseClicked

    private void nuevoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nuevoMouseClicked
        // TODO add your handling code here:
        modificar.setSelected(false);
        if(nuevo.isSelected()){
            
            gclave.setEnabled(true);
            gcosto.setEnabled(true);
            gdescripcion.setEnabled(true);
            gclave.setText("");
            gcosto.setText("");
            gdescripcion.setText("");
            id.setText("");
        }
        
        if(!modificar.isSelected() && !nuevo.isSelected()){
            gclave.setEnabled(false);
            gcosto.setEnabled(false);
            gdescripcion.setEnabled(false);
            
        }
    }//GEN-LAST:event_nuevoMouseClicked

