    private void guardaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_guardaMousePressed
        // TODO add your handling code here:
        String status = updateproceso.getSelectedItem().toString();
        if(newcheck.isSelected()){
            //aquí poner el insertarasignación
            String nombrep = insertaproyecto.getSelectedItem().toString();
            String actualizacion = textoactualizaciones.getText();
            
            if(nombrep.equals("Proyecto") || status.equals("Proceso") || actualizacion.equals("")){
                JOptionPane.showMessageDialog(null,"Llene todos los campos.");
            }else{
                if(status.equals("Finalizado")){
                   JOptionPane.showMessageDialog(null,"Seleccione un estatus válido."); 
                }else{
                tabla.insertarasignacion(nombrep, status, actualizacion);
                tabla.getidasignacion();
                tabla.tablavistaasignaciones(tablaasignacion);
                idasignacion.setText("");
                updateproceso.setSelectedIndex(0);
                insertaproyecto.setSelectedIndex(0);
                textoactualizaciones.setText("");
                insertaproyecto.setEnabled(false);
                textoactualizaciones.setEnabled(false);
                tabla.insertarpersonalasignado(tablaasignados,tabla.setidasignacion());
                tabla.tablaasignados(tablaasignados);
                
                }
            }
        }
  
        
        if(!newcheck.isSelected()){
            //aquí poner el updateasignación
            if(status.equals("Finalizado")){
                   //añadir pantalla de seguro?
                   JOptionPane.showMessageDialog(null,"¿Seguro quiere finalizar la asignación?."); 
                   //añadir updatenormal
                  tabla.updateasignacion(idasignacion.getText(),status);
                  //deshabilitar personalasignado
                }else{
                  int idAsignacion = Integer.parseInt(idasignacion.getText());
                  tabla.updateasignacion(idasignacion.getText(),status);
                  //tabla.updatepersonalasignado(tablaasignados,idasignacion.getText());
                  tabla.insertarpersonalasignado(tablaasignados,idAsignacion);
                  tabla.mostrarpersonal(tablaasignados,idasignacion.getText());
            }
        }
        newcheck.setSelected(false);
        
    }//GEN-LAST:event_guardaMousePressed

