    private void tablahubMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablahubMouseClicked
        // TODO add your handling code here:
      
      try {
            // TODO add your handling code here:
            
            tabla.mostrarproyecto(tablahub,nombrep, pcotiza, streamp, filesize,deadline);
             
        
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(MenuProyecto.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } catch (IOException ex) {
            java.util.logging.Logger.getLogger(MenuProyecto.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            java.util.logging.Logger.getLogger(MenuProyecto.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    int filaSeleccionada = tablahub.getSelectedRow();
    int columnaContrato = getColumnaPorNombre("Contrato"); // Ajusta este valor al índice real de la columna "contrato"
    
    if (filaSeleccionada != -1) { // Verificar si se ha seleccionado alguna fila
        Object valorCelda = tablahub.getValueAt(filaSeleccionada, columnaContrato);
        if (valorCelda != null && !valorCelda.toString().isEmpty()) { // Verificar si el valor de la celda no es nulo ni vacío
            nombrecontrato.setText("ArchivoCargado.pdf");
        } else {
            nombrecontrato.setText("sinarchivo.pdf");
        }
    }
    }//GEN-LAST:event_tablahubMouseClicked
    
    //Método para extraer string de la columna del archivo
    private int getColumnaPorNombre(String nombreColumna) {
    for (int i = 0; i < tablahub.getColumnCount(); i++) {
        if (tablahub.getColumnName(i).equals(nombreColumna)) {
            return i;
        }
    }
    return -1; // Retorna -1 si no se encuentra la columna
}
    
