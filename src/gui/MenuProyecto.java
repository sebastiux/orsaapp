    private void guardapMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_guardapMousePressed
        // TODO add your handling code here:
      
      String savefecha;
      Date selectedDate;
      SimpleDateFormat dateFormat;
      if(nuevo.isSelected()){
        selectedDate = deadline.getDate();
        dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        if(selectedDate != null){
            savefecha = dateFormat.format(selectedDate);
        } else {
             savefecha = null;
         }
        tabla.crearproyecto(nombrep, pcotiza, streamp, filesize, savefecha);
        //tabla.tablamenup(tablahub);
        tablahub.repaint();
        tablahub.revalidate();
        streamp = null;//Limpiar variable de archivo
        }
      if(modificar.isSelected()){
        selectedDate = deadline.getDate();
        dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        if(selectedDate != null){
            savefecha = dateFormat.format(selectedDate);
        } else {
             savefecha = null;
         } 
          if(streamp==null && streamp2!=null){
          streamp2 = tabla.setcontrato();
          try {
             filesize2 = tabla.setcontrato().available();
             tabla.updateproyecto(tablahub, nombrep, pcotiza,streamp2,filesize2, savefecha);
          } catch (IOException ex) {
              java.util.logging.Logger.getLogger(MenuProyecto.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
          }
        }else{
             tabla.updateproyecto(tablahub, nombrep, pcotiza,streamp,filesize, savefecha);//La opción cuando hay un nuevo archivo
          }
        
        //tabla.tablamenup(tablahub);
        tablahub.repaint();
        tablahub.revalidate();
      }
      
       nombrep.setText("");
       pcotiza.setText("");
       deadline.setDate(null);
       nombrecontrato.setText("sinarchivo.pdf");
      
       // Update the table content
        tabla.muestratablamenup(tablahub);

    // Repaint and revalidate to ensure the table is updated
        tablahub.repaint();
        tablahub.revalidate();
       
        
    }//GEN-LAST:event_guardapMousePressed

