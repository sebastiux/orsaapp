    private void filtraherramientaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_filtraherramientaMousePressed
        // TODO add your handling code here:
        String nombrep;
        String estatus;
        TablasDashboard tabladashboard = new TablasDashboard();

        nombrep = (String) buscaherramienta.getSelectedItem();
        estatus = (String) filtrodisponible.getSelectedItem();
        if(nombrep.equals("Herramienta")){
            nombrep = null;
        }
        
        if(estatus.equals("Disponibilidad")){
            estatus = null;
        }

        
        tabladashboard.filtroherramienta(tablahm, estatus, nombrep);
    }//GEN-LAST:event_filtraherramientaMousePressed

